def stem(words):
    end = ["es", "ed", "ing","s", "ly","ies","ied","ous","ment","able","ness","al","er","est","ful","ic","y",'ned']
    for word in words:
        # Remove any suffixes.
        for e in end:
            if word.endswith(e):
                word = word.rstrip(e)

    return words